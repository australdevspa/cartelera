import Sala from '@/components/Sala'
import SalaVacia from '@/components/SalaVacia'

export default {
  name: 'Piso4',
  components: {
		Sala,
		SalaVacia
  },
  data () {
    return {
			loading_sala: true,
			data_programa: []
    }
  },
  computed: {
    salas() {
      return this.$store.state.salas;
    },
    programacion() {
      return this.$store.state.programacion;
		},
		sala_seleccionada() {
      return this.$store.state.sala_especifica;
    },
  },
  methods: {
  	cargarModalSala(x) {
			this.loading_sala = true
			var number = x.currentTarget.id;
			this.$store.dispatch('loadSalaEspecifica', x.currentTarget.id)
				.then(response => {
						this.loading_sala = false;
				})
				.catch(error => {
						this.loading_sala= true;
				})
			this.data_programa = []
			for(var i = 0; i < this.programacion.length; i++){
				if(this.programacion[i].area === number){
					this.data_programa = this.programacion[i]
				} 
			}
		},
		tieneActividades(id) {
			var x = this.programacion.filter((item) => item.area.includes(id));
			if(x.length == 0){
				return false
			}else{
				return true
			}
		}	
  }
}
